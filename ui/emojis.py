    @classmethod
    def get_health_bar(cls, vida_atual: int, vida_max: int, tamanho: int = 10) -> str:
        """Retorna uma barra de vida colorida"""
        percentual = vida_atual / vida_max
        
        if percentual > 0.6:
            cor = cls.Barras.VERDE
        elif percentual > 0.3:
            cor = cls.Barras.AMARELO
        else:
            cor = cls.Barras.VERMELHO
        
        cheios = int((vida_atual / vida_max) * tamanho)
        return cor * cheios + cls.Barras.BRANCO * (tamanho - cheios)
    
